export class TwoTuple<A, B> {
    public a: A;
    public b: B;

    constructor(a: A, b: B) {
        this.a = a;
        this.b = b;
    }
}

export class ThreeTuple<A, B, C> {
    public a: A;
    public b: B;
    public c: C;

    constructor(a: A, b: B, c: C) {
        this.a = a;
        this.b = b;
        this.c = c;
    }
}
